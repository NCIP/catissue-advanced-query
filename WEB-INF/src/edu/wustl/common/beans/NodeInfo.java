package edu.wustl.common.beans;

import java.sql.Date;


public class NodeInfo
{
	Object obj;
	Date dob;
	
	/**
	 * @return the obj
	 */
	public Object getObj()
	{
		return obj;
	}
	
	/**
	 * @param obj the obj to set
	 */
	public void setObj(Object obj)
	{
		this.obj = obj;
	}
	
	/**
	 * @return the dob
	 */
	public Date getDob()
	{
		return dob;
	}
	
	/**
	 * @param dob the dob to set
	 */
	public void setDob(Date dob)
	{
		this.dob = dob;
	}
	
	

}
